      _showErrorSnackBar('Ïù¥Î©îÏùºÏùÑ ÏûÖÎ†•Ìï¥Ï£ºÏÑ∏Ïöî.');
      return;
    }

    final authService = Provider.of<AuthService>(context, listen: false);
    final result =
        await authService.resetPassword(_emailController.text.trim());

    if (mounted) {
      if (result.success) {
        _showSuccessSnackBar('ÎπÑÎ∞ÄÎ≤àÌò∏ Ïû¨ÏÑ§Ï†ï Ïù¥Î©îÏùºÏùÑ Ï†ÑÏÜ°ÌñàÏäµÎãàÎã§.');
      } else {
        _showErrorSnackBar(result.errorMessage!);
      }
    }
  }

  void _handleGuestMode() {
    final authService = Provider.of<AuthService>(context, listen: false);
    authService.continueAsGuest();
    // Í≤åÏä§Ìä∏ Î™®ÎìúÎ°ú Í∂åÌïú ÏÑ§Ï†ï ÌôîÎ©¥ÏúºÎ°ú Ïù¥Îèô
